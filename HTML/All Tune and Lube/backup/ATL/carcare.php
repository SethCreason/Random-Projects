            Tip of the Week:
            <BR><strong>Ways to Save Gas,  By Lucy Lazarony</strong><br><br>
            <b>Fill up with a lower-octane gasoline.</b> Buy the lowest grade or octane of gasoline that is appropriate for your car. Unless your car requires premium gasoline, filling up your car with high-octane fuel is a waste of money. That pricey premium fuel won't boost your car's fuel economy or performance in the least, so skip it.

If you're not sure what grade of fuel works best for your car, open up your owner's manual and take a look. As long as your engine doesn't knock or ping when you fuel up with regular unleaded, you're good to drive on this much cheaper gas. Passing on pricey premium gasoline could save you hundreds of dollars a year.
<br><br>
<b>Don't top off.</b> Don't bother topping off when filling your car's gas tank. Any additional gas is just going to slop around or seep out. Why waste your money paying for gas your car won't use? Stop pumping at the first indication that your tank is full when the automatic nozzle clicks off.
<br><br>
<b>Keep your engine in tune.</b> Fixing a car that is out of tune or has failed an emissions test can boost gas mileage by about 4 percent. So be sure to give your car regular tune-ups. You'll also want to watch out for worn spark plugs. A misfiring spark plug can reduce a car's fuel efficiency by as much as 30 percent.
<br><br>
<b>Replace air filters.</b> Keep a close eye on your engine's air filter. When the engine air filter clogs with dirt, dust and bugs, it causes your engine to work harder and your car becomes less fuel-efficient. Replacing a clogged air filter could improve your gas mileage by as much as 10 percent and save you 15 cents a gallon. It's a good idea to have your engine air filter checked at each oil change. The Car Care Council recommends changing your car's air and oil filters every three months or 3,000 miles or as specified in your owner's manual.
<br><br>
<b>Use the right oil.</b> You can improve your car's gas mileage by 1 percent to 2 percent by using the manufacturer's recommended grade of motor oil. Opt for motor oil with the words "energy conserving" on the API performance label. This oil contains friction-reducing additives.
<br><br>
<b>Don't skimp on maintenance.</b> Be serious about auto care. Your car's performance depends on it. </div>
